char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	while (s[i] != (char)c && i >= 1)
		i--;
	if (s[i] == c)
		return ((char *)&s[i]);
	return (0);
}
